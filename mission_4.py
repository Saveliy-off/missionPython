# Задача: Магическая колода карт.

# Описание: Вам предстоит создать программу, представляющую магическую колоду карт. Колода должна содержать различные карты, каждая из которых обладает свойствами и способностями. 
# Колода должна также иметь возможность тасования и выдачи карт.

# Требования:

# Создайте базовый класс Card, представляющий отдельную карту в колоде. Каждая карта должна иметь следующие атрибуты: название карты, значение и магическую способность (например, лечение, урон, уклонение и т.д.).

# Создайте классы, представляющие различные типы карт: AttackCard, HealCard, DefenseCard, DodgeCard, и т.д. Каждый из этих классов должен наследоваться от класса Card и переопределять специфические свойства и способности для своего типа.

# Создайте класс Deck, представляющий колоду карт. Колода должна содержать список карт, а также методы для тасования колоды и выдачи карты из вершины колоды.

# Реализуйте класс Player, представляющий игрока, у которого есть своя колода карт. У игрока должен быть атрибут с его текущим здоровьем и методы для того, чтобы игрок мог:

# Выбрать карту из своей колоды для использования в ходе.
# Применить магическую способность выбранной карты к другому игроку или себе.
# Создайте класс Game, который будет управлять игровым процессом. У класса должны быть методы для:

# Создания двух игроков с их собственными колодами карт.
# Проведения ходов между игроками, где каждый игрок выбирает карту для использования.
# Применения магических способностей карт и обновления состояния игроков после хода.
# Реализуйте пример игрового процесса, в котором два игрока будут использовать свои карты, применять магические способности и пытаться выиграть игру, пока у одного из них не закончатся очки здоровья.

# В программе учтите возможные ошибки ввода данных (например, попытка использовать карту, которой у игрока уже нет).

# Это задача, требующая хорошего понимания ООП, наследования, полиморфизма и взаимодействия между классами. Успехов в её решении!